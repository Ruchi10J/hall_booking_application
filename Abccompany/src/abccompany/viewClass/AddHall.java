    private void addHallBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addHallBtnActionPerformed
        // TODO add your handling code here:
        
        Hall obj=new Hall();
        int hId=Integer.parseInt(editIdHall.getText());
        String hType=cmbeditHallType.getModel().getSelectedItem().toString();
        String hName=editHallName.getText();
        double fee=Double.parseDouble(editHallPrice.getText());
        
        obj.setIdHall(hId);
        obj.setType(hType);
        obj.setHallName(hName);
        obj.setHallPrice(fee);
        
        HallOperationsImplement imp= new HallOperationsImplement();
        imp.save(obj);
        
        Load();
        
        editIdHall.setText("");
        cmbeditHallType.setSelectedItem("");
        editHallName.setText("");
        editHallPrice.setText("");
        cmbeditHallType.requestFocus();;
        
    }//GEN-LAST:event_addHallBtnActionPerformed

    
