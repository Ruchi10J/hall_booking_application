    int search;
    private void searchHallBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchHallBtnActionPerformed
        // TODO add your handling code here:
        
        search = Integer.parseInt(JOptionPane.showInputDialog("Enter Hall ID"));

    HallOperationsImplement dao = new HallOperationsImplement();
    Hall st = dao.getDetails(search);

    // Set the hall details to the text fields
    editHallName.setText(st.getHallName());
    editHallPrice.setText(String.valueOf(st.getHallPrice()));

    // Set the relevant value in the combo box
    String type = st.getHallType();
    cmbeditHallType.setSelectedItem(type);
        
        
        
  
    }//GEN-LAST:event_searchHallBtnActionPerformed

